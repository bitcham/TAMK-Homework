// 1. Number
let num = 42;
console.log("num is a " + typeof num);

// 2. String  
let str = "Hello, world!";
console.log("str is a " + typeof str);

// 3. Boolean
let bool = true;
console.log("bool is a " + typeof bool);

// 4. Null
let nullVar = null;
console.log("nullVar is a " + typeof nullVar);

// 5. Undefined
let undefinedVar = undefined;
console.log("undefinedVar is " + typeof undefinedVar);

// 6. Symbol
let sym = Symbol();
console.log("sym is a " + typeof sym);

// 7. Object
let obj = {
   key1: "value1",
   key2: "value2"
};
console.log("obj is an " + typeof obj);